#include <iostream>
#include <algorithm>
#include <vector>
#include <sstream>

using namespace std;

int main(){
	ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	int n;
	cin >> n;
	
	for (int i = n; i > 0; i--)
	{
		for (int j = n - i; j > 0; j--)
			cout << ' ';
		for (int k = 0; k < 2 * i - 1; k++)
			cout << '*';

		cout << '\n';
	}

	return 0;
}